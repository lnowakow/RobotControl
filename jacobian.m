function [J] = jacobian(q)

l = [0.6731; 0.432; 0.434];
s1 = sin(q(1));
c1 = cos(q(1));
s2 = sin(q(2));
c2 = cos(q(2));
s23 = sin(q(2) + q(3));
c23 = cos(q(2) + q(3));

J = zeros(3,3);

J(1, 1) = -s1*( l(2)*c2 + l(3)*s23);
J(1, 2) =  c1*(-l(2)*s2 + l(3)*c23);
J(1, 3) = l(3)*c1*c23;

J(2, 1) = c1*( l(2)*c2 + l(3)*s23);
J(2, 2) = s1*(-l(2)*s2 + l(3)*c23);
J(2, 3) = l(3)*s1*c23;

J(3, 2) = -l(2)*c2 - l(3)*s23;
J(3, 3) = -l(3)*s23;